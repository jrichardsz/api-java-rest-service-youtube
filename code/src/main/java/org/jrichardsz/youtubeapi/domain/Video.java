package org.jrichardsz.youtubeapi.domain;

public class Video {

	public String thumbnailUrlImage;
	public String videoTitle;

	public String getThumbnailUrlImage() {
		return thumbnailUrlImage;
	}

	public void setThumbnailUrlImage(String thumbnailUrlImage) {
		this.thumbnailUrlImage = thumbnailUrlImage;
	}

	public String getVideoTitle() {
		return videoTitle;
	}

	public void setVideoTitle(String videoTitle) {
		this.videoTitle = videoTitle;
	}



}
